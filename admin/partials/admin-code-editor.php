jQuery(document).ready(function($) {
    if (typeof wp.codeEditor !== 'undefined') {
        var editor = wp.codeEditor.initialize($('#custom_js'), {
            codemirror: {
                mode: 'javascript',
                lineNumbers: true,
                lineWrapping: true,
                theme: 'default',
                autoCloseBrackets: true,
                matchBrackets: true,
                indentUnit: 4,
                tabSize: 4,
                indentWithTabs: true,
                extraKeys: {
                    'Ctrl-Space': 'autocomplete',
                    'Ctrl-S': function() {
                        $('form').submit();
                    }
                }
            }
        });

        var jsEditor = editor.codemirror;

        jsEditor.on('change', function() {
            // No auto-save
        });
    }

    $('form').on('submit', function(e) {
        var jsCode = $('#custom_js').val();
        if (jsCode.includes('</script>')) {
            if (!confirm('<?php echo esc_js( __( 'Your code contains a </script> tag. This may break the widget. Continue?', 'magicsquare-widget' ) ); ?>')) {
                e.preventDefault();
                return false;
            }
        }
        return true;
    });
});
